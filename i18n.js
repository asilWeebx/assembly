function getLang() {
  return localStorage.getItem(I18N_STORAGE_KEY) || DEFAULT_LANG;
}

function setLang(lang) {
  localStorage.setItem(I18N_STORAGE_KEY, lang);
}

function t(page, key, lang) {
  const dict = window.I18N;
  if (!dict) return key;

  // Check common first
  const commonValue = dict.common?.[lang]?.[key];
  if (commonValue) return commonValue;

  // Then check page-specific
  return dict[page]?.[lang]?.[key] || dict[page]?.ru?.[key] || key;
}

function detectPageKey() {
  const path = location.pathname;
  const file = path.split("/").pop() || "index.html";
  console.log("[i18n] Path:", path, "Detected File:", file);

  if (file === "index.html" || file === "" || file.includes("index")) return "index";
  if (file.includes("about")) return "about";
  if (file.includes("services")) return "services";
  if (file.includes("associations")) return "associations";
  if (file.includes("projects")) return "projects";
  if (file.includes("news")) return "news";
  if (file.includes("contacts")) return "contacts";

  // Service Pages
  if (file.includes("service-smart-city")) return "smartCity";
  if (file.includes("service-networking")) return "networking";
  if (file.includes("service-invest-hub")) return "investHub";
  if (file.includes("service-bizness-darja") || file.includes("service-biznes-darja")) return "biznessDarja";
  if (file.includes("service-edu-jo")) return "eduJob";
  if (file.includes("service-reportaj-go")) return "reportajGo";
  if (file.includes("service-gr")) return "governmentRelations";
  if (file.includes("service-fr")) return "foreignRelations";
  if (file.includes("service-br")) return "businessRelations";

  // Legacy or non-service paths
  if (file.includes("invest-hub")) return "investHub";
  if (file.includes("smart-city")) return "smartCity";
  if (file.includes("bizness-darja") || file.includes("biznes-darja")) return "biznessDarja";
  if (file.includes("edu-jo")) return "eduJob";
  if (file.includes("reportaj-go")) return "reportajGo";

  if (file.includes("admin")) return "admin";

  return "index";
}

function applyTranslations(lang) {
  const page = detectPageKey();
  const currentLang = lang || getLang();
  console.log("[i18n] Applying translations for page:", page, "Lang:", currentLang);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const translation = t(page, key, currentLang);

    if (node.tagName === "INPUT" || node.tagName === "TEXTAREA") {
      node.placeholder = translation;
    } else if (node.hasAttribute("data-i18n-target")) {
      const target = node.getAttribute("data-i18n-target");
      if (target === "placeholder") {
        node.placeholder = translation;
      } else {
        node.setAttribute(target, translation);
      }
    } else {
      // Robust text node update: iterate through all top-level child nodes
      // and update the first non-empty text node. This handles cases where
      // the text follows an icon or other element.
      let translated = false;
      for (const child of node.childNodes) {
        if (child.nodeType === Node.TEXT_NODE && child.textContent.trim()) {
          child.textContent = translation;
          translated = true;
          break;
        }
      }
      // Fallback: if no text node found but element is empty, set textContent
      if (!translated && node.childNodes.length === 0) {
        node.textContent = translation;
      }
    }
  });

  // Update page title if key exists
  const titleVal = t(page, "pageTitle", currentLang);
  if (titleVal !== "pageTitle") {
    document.title = titleVal;
  }
}

// Export to window so shared-nav.js can call it after nav is injected
window.applyTranslations = applyTranslations;


function createLangSwitcher() {
  const holders = document.querySelectorAll("[data-lang-switcher]");
  if (!holders.length) return;
  const current = getLang();
  holders.forEach((holder) => {
    if (holder.querySelector("[data-shared-lang]")) return;

    const dict = window.I18N;
    const labelText = (dict && dict.common && dict.common[current]) ? dict.common[current].lang : "Language";
    holder.innerHTML = `
      <label class="text-xs text-slate-300 mr-2">${labelText}</label>
      <select class="langSelect rounded-lg bg-[#0f1a31] text-[#dae2fd] border border-[#93ccff]/25 px-2 py-1 text-sm">
        <option value="ru">RU</option>
        <option value="uz">UZ</option>
        <option value="en">EN</option>
      </select>
    `;
    const select = holder.querySelector(".langSelect");
    select.value = current;
    select.addEventListener("change", (e) => {
      setLang(e.target.value);
      location.reload(); // Reload to sync with shared-nav.js and other multi-page scripts
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const lang = getLang();
  createLangSwitcher();
  applyTranslations(lang);
});
